    private void btnAnalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalizarActionPerformed
        // TODO add your handling code here:
        File arq = new File("teste.txt");
        PrintWriter escrever;
        try {
            escrever = new PrintWriter(arq);
            escrever.print(txtEntrada.getText());
            escrever.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        try {
            Reader leitor = new BufferedReader(new FileReader(chooser.getSelectedFile()));
            Lexer lexer = new Lexer(leitor);
            String resultado = "";
            while(true){
                Tokens tokens = lexer.yylex();
                if(tokens == null){
                    resultado+="FIM";
                    txtResultado.setText(resultado);
                    return;
                }
                switch (tokens) {
                    case ERROR:
                        resultado += "\tesse simbolo não existe\t\n";
                        break;
                    case Identificador: case Reservadas:  case  Numero:
                        resultado += lexer.lexeme +"\tÉ um token tipo:\t" + tokens + "\n";
                        break;
                   
                    default:
                        resultado += "\tTokens:\t" + tokens + "\n";
                        break;
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrMain.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FrMain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAnalizarActionPerformed

